                    The Commission has a separate Secretariat at New Delhi, consisting of about 300 officials, in a hierarchical set up.</p>

                <h2>INDEPENDENT ELECTION COMMISSION</h2>
                <p>The Election Commission of India is an independent Constitutional Authority since 25th January, 1950. The Constitution provides for Chief Election Commissioner and one or more Election Commissioners to be appointed. The Commission consisted of Chief Election Commissioner till 1989, when for the first time two Election Commissioners were appointed. Currently, the Commission has a Chief Election Commissioner and two Commissioners. Chief Election Commissioner and the Election Commissioners enjoy the status of a Judge of the Supreme Court of India. The Chief Election Commissioner can be removed from office only by Parliamentary impeachment. The Election Commission is responsible for the conduct of elections to parliament and state legislatures and to the offices of the President and Vice-President. The Election Commission prepares, maintains and periodically updates the Electoral Rolls, which show who is entitled to vote, supervises the nomination of candidates, registers political parties, monitors the election campaign, including the funding and exponential by candidates. It also facilitates the coverage of the election process by the media, organises the polling booths where voting takes place, and looks after the counting of votes and the declaration of results. All this is done to ensure that elections can take place in an orderly and fair manner. The Commission decides most matters by consensus but in case of any dissension, the majority view prevails. The Commission has its headquarters in New Delhi, with a Secretariat of some 350 staff members. At the state level, a Chief Electoral Officer with a core staff of varying numbers, is available on a full time basis. At the district and constituency level, officers and staff of the civil administration double up as election officials. During actual conduct of elections, a vast number of additional staff are temporarily drafted for about two weeks. They function mainly as polling and counting officials.</p>

                <h2>WHO CAN VOTE?</h2>
                <p>The democratic system in India is based on the principle of universal adult suffrage; that any citizen over the age of 18 can vote in an election (before 1989 the age limit was 21). The right to vote is irrespective of caste, creed, religion or gender. Those who are deemed unsound of mind, and people convicted of certain criminal offences are not allowed to vote. There has been a general increase in the number of people voting in Indian election. In 1996, 57.4% of the electorate voted. This increased to 66 % in the General Election held in 2014. Women voted in good numbers and almost in equal proportion as men.</p>

                <h2> THE ELECTORAL ROLL </h2>
                <p> The electoral roll of a constituency is a list of all those people in that constituency who are registered to vote in the elections. Only those people whose names are there in the electoral rolls are allowed to vote as ‘electors’. The electoral roll is normally revised every year to add the names of those who are not less than 18 on a qualifying date years as on the first day of January of that year, or have moved into the constituency, and to remove the names of those who have died or moved out of the constituency. The updating of electoral rolls is a continuous process, which is interrupted only at the time of the elections during the period from after the last date of filing nominations till the completion of the elections. The administrative machinery involved in the preparation, maintenance and revision of the electoral rolls has the ECI at the top of the hierarchy. According to Section 13B of the Representation of the People Act, 1950, the electoral roll for each constituency in a State/UT is to be prepared and revised by an Electoral Registration Officer (ERO). At the bottom of the hierarchy, Booth Level Officers (BLOs) and supervisors are also appointed. Each BLO has one or two polling stations under his/her jurisdiction. During the revision of the electoral rolls, BLOs may be assigned the tasks of enumeration, the verification of rolls and forms, and the collection of forms and photographs from the electors for Electoral Photo Identity Card (EPIC) and photo roll maximisation. BLOs hand over the forms thus collected to the designated officers and EROs for further action. During the time when continuous revision and updating is going on, BLOs may be used for the identification of dead and shifted voters on specified dates prescribed for the purpose by the ECI (one week in each half of a year). In an election year, a BLO’s task begins with the publication of the draft rolls till the completion of the second Supplement according to a specific programme approved by the ECI. Supervisory officers maintain checks on the quality of work done by the BLOs, and closely monitor it. Each Supervisory Officer has 10-20 BLOs under his/her supervision. Apart from the machinery involved in the process of the preparation and revision of the electoral rolls, community participation has also been identified as one of the ways in which political parties can appoint their representatives as Booth Level Agents (BLAs) on the pattern of appointment of Polling Agents, to complement the task of BLOs. Normally, one BLA may be appointed for each part of the electoral roll. The BLA must be a registered elector in the relevant part of the electoral roll for which he/she is appointed, as it is expected that the BLA will scrutinise the entries in the draft roll of the area where he/she resides, in order to identify the entries of dead persons and shifted persons.</p>

<h2>
	THE ELECTORAL EVENT &nbsp;- WHEN DO ELECTIONS TAKE PLACE?
</h2>

<p>
	Elections for the Lok Sabha and every State Legislative Assembly have to take place every five years, unless called earlier. The President can dissolve Lok Sabha and call a general election before five years are up, if the government can no longer command the confidence of the Lok Sabha, and if there is no alternative government available to take over. General elections to the Lok Sabha took place in 1952, 1957, 1962, 1967, 1971, 1977, 1984, 1989, 1991, 1996, 1997, 1998, 1999, 2004, 2009 and 2014. Holding of regular elections can only be deferred by means of a constitutional amendment and in consultation with the Election Commission, and it is recognised that interruptions of regular elections are acceptable only in extraordinary circumstances. When the five-year limit is up, or the legislature has been dissolved and new elections have been called, the Election Commission puts into effect the process for holding an election.&nbsp;
</p>

<h2>
	SCHEDULING OF ELECTION
</h2>

<p>
	The election process starts with the issue of notification for the Parliamentary Constituencies and Assembly Constituencies. As per legal provisions, a period of seven days is provided for the filing of nominations after the notification is issued. The scrutiny of the nominations is carried out on the day following the last date for nominations. Thereafter, two days are provided for the withdrawal of nominations and the final list of candidates is prepared after the withdrawal. The campaign period is usually of 14 days or more, and the campaign comes to an end 48 hours before the close of the polls in the respective constituencies.
</p>

<h2>
	DEPLOYMENT OF POLLING PERSONNEL
</h2>

<p>
	Another important aspect is the deployment of poll personnel. This is done through a three-stage randomisation process, which is as follows:&nbsp;
</p>

<p>
	<strong>First Stage:</strong> At this stage, the purpose is to identify and select the required number of polling personnel for the District. In the appointment letter the identity of the Assembly Constituency (AC) is not to be disclosed. Polling personnel will know whether he/she is a Presiding Officer (PrO) or a Polling Officer (PO), the venue and time of training. The presence of Observers is not required at this stage.
</p>

<p>
	<strong>Second Stage:</strong> Polling parties are formed at this stage. The AC may be known, but the actual Polling Station (PS) is not known. Observers must be present. This randomisation is not to be done before 6/7 days from the day of the poll.&nbsp;
</p>

<p>
	<strong>Third Stage:</strong> At the time of the dispersal of the polling party, the allocation of the PS is done. The presence of Observers is a must and the certificate regarding the formation of polling parties on the basis of the three-stage randomisation process needs to be given by the DEO to the <abbr title="Election Commission of India">ECI</abbr> and separately to the CEO.<br>
	In the 2014 General Elections, the arrangements at the polling stations were reviewed and instructions were issued to have a minimum guaranteed environ at the polling stations, comprising certain Basic Minimum Facilities (BMF) such as drinking water, shade/shelter, light, ramps and so on. The voting compartment was standardised for all States and UTs by issuing instructions to set them up in such a way that the secrecy of the ballot was not compromised and prohibited materials such as jute bags and plastic sheets were not used.
</p>

<h2>
	WHO CAN STAND FOR ELECTION
</h2>

<p>
	Any Indian citizen who is registered as a voter is otherwise not disqualified under the Law and is over 25 years of age is allowed to contest elections to the Lok Sabha or State Legislative Assemblies. For the Rajya Sabha the age limit is 30 years. Candidates for Vidhan Sabha should be residents of the same state from which they wish to contest. Every candidate has to make deposit of Rs. 25,000/- for Lok Sabha election and Rs. 10,000/- for Rajya Sabha or Vidhan Sabha elections, except for candidates from the Scheduled Castes and Scheduled Tribes who pay half of these amounts. The deposit is returned if the candidate receives more than one-sixth of the total number of valid votes polled in the constituency. Nominations must be supported at least by one registered elector of the constituency, in the case of a candidate sponsored by a recognised Party and by ten registered electors from the constituency in case of other candidates. Returning Officers, appointed by the Election Commission, are put in charge to receive nominations of candidates in each constituency, and oversee the formalities of the election. In a number of seats in the Lok Sabha and the Vidhan Sabha, the candidates can only be from either one of the scheduled castes or scheduled tribes. The number of these reserved seats is meant to be approximately in proportion to the number of people from scheduled castes or scheduled tribes in each state. There are currently 84 seats reserved for the scheduled castes and 47 reserved for the scheduled tribes in the Lok Sabha.
</p>







